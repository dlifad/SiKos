    public String getUsername() { 
        return jTextField_username.getText().trim(); 
    }
    public String getEmail() { 
        return jTextField_email.getText().trim(); 
    }
    public String getPassword() { 
        return new String(jPasswordField_pw.getPassword()).trim();
    }
    public String getKonfirmasiPassword() { 
        return new String(jPasswordField_confirmPW.getPassword()).trim();
    }

    public void tampilkanPesan(String pesan, String judul, int messageType) {
        JOptionPane.showMessageDialog(this, pesan, judul, messageType);
    }

    public void registrasiBerhasil() {
        JOptionPane.showMessageDialog(this, "Registrasi berhasil! Silakan login.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        new LoginView().setVisible(true);
        this.dispose();
    }

    public void registrasiGagal(String pesanError) {
        JOptionPane.showMessageDialog(this, pesanError, "Registrasi Gagal", JOptionPane.ERROR_MESSAGE);
    }

